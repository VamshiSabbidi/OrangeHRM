package com.DataTypes;

public class DatatypesExample
{
public static void main(String args[]){
	
	byte bytevariable=10;
	System.out.println("The value of variable bytevariable is :" + bytevariable);
	
	short shortvariable=20;
	System.out.println("The value of variable shortvariable is :" +shortvariable);
	
	int intvariable=20;
	System.out.println("The value of variable intvariable is :" +intvariable);
	
	long longvariable=40;
	System.out.println("The value of variable longvariable is :" +longvariable);
	
	double doublevariable=12.123456789101112;
	System.out.println("The value of variable doublevariable is :" +doublevariable);
	
	float floatvariable=55.6789f;
	System.out.println("The value of variable floatvariable is :" +floatvariable);
	
	char charvariable='v';
	System.out.println("The value of variable charvariable is :" +charvariable);
	
	String stringvariable="vamshi";
	System.out.println("The value of variable stringvariable is :" +stringvariable);
	
	boolean booleanvariable=true;
	System.out.println("The value of variable booleanvariable is :" +booleanvariable);	
}
}
