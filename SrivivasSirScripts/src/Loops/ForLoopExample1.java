package com.Loops;
public class ForLoopExample1 {
	public static void main(String[] args) {
		
		for(int a=1;a<=3;a++)
		{
			System.out.println(a+" LiveTech ");
		}
		
		System.out.println(" @@@@@@@@@@@@@@@@@@@ ");
		
		for(int i=2;i<=8;i=i+2)
		{
			System.out.println(i + " Selenium ");
		}
		
		System.out.println(" $$$$$$$$$$$$$$$$$$ ");
		
		for(int k=-5;k<=0;k=k+3)
		{
			System.out.println(k);
		}
		
		System.out.println(" $$$$$$$$$$$$$$$$ ");
		
		
		for(int p=-10;p>=-6;p=p-3)
		{
			System.out.println(p+" Selenium ");
		}
		
		
		System.out.println(" ***************** ");
		
		for(int i=9;i>1;i=i-2)
		{
			System.out.println(i+" LoadRunner ");
		}
		
		System.out.println(" &&&&&&&&&&&&&&&&&&&&&&&&&&&&&& ");
				
	}
}
